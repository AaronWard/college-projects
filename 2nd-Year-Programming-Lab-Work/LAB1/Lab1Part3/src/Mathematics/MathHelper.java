package Mathematics;

/**
 * A class to calculate the string parameters
 * @author aaron
 *
 */
public class MathHelper
{
	/**
	 * This method takes the two parameters
	 * and multiplies them. Then returns them to the 
	 * Application Class.
	 * @param x
	 * @param y
	 * @return
	 */
	public int multiplyNums(int x, int y)
	{	
		//multiply parameters
		int result = x * y;
		return result;
	}

}
